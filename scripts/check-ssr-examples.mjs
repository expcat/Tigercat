#!/usr/bin/env node

import { spawnSync } from 'node:child_process'
import { readFileSync } from 'node:fs'
import { join } from 'node:path'

const root = join(import.meta.dirname, '..')
const nextEnvPath = join(root, 'examples/nextjs/next-env.d.ts')

const before = readFileSync(nextEnvPath, 'utf8')
const pnpmCommand = process.platform === 'win32' ? 'pnpm.cmd' : 'pnpm'
const result = spawnSync(pnpmCommand, ['example:ssr:build'], {
  cwd: root,
  stdio: 'inherit'
})

if (result.error) {
  console.error(result.error)
  process.exit(1)
}

if (result.status !== 0) {
  process.exit(result.status ?? 1)
}

const after = readFileSync(nextEnvPath, 'utf8')

if (before !== after) {
  console.error('examples/nextjs/next-env.d.ts changed during SSR example build.')
  console.error('Keep this file in the format generated by Next.js before running the check again.')
  process.exit(1)
}

console.log('SSR example build completed without next-env.d.ts drift.')
